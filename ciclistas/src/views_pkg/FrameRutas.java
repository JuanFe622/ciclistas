    void show_routes(){
        String user = txtUserDocument.getText();
        System.out.println(user);
        String query = "SELECT * FROM tb_route WHERE id_cyclist = '"+user+"' ";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            Object[] route = new Object[8];
            model = (DefaultTableModel)tbl_routes.getModel();
            while(rs.next()){
                route[0]=rs.getInt("id");
                route[1]=rs.getString("route_name");
                route[2]=rs.getInt("route_distance");
                route[3]=rs.getDate("start_date");
                route[4]=rs.getDate("end_date");
                route[5]=rs.getTime("start_hour");
                route[6]=rs.getTime("end_hour");
                route[7]=rs.getInt("id_cyclist");
                model.addRow(route);
            }
            tbl_routes.setModel(model);
        } catch(SQLException e){
            JOptionPane.showConfirmDialog(this, "No se encontraron registros de rutas para el usuario en cuestion");
        }
    }
    
    boolean validacionFecha(Date inicio, Date fin){
        Calendar c1= Calendar.getInstance();
        Calendar c2= Calendar.getInstance();
        c1.set(Calendar.MONTH, 7);
        c1.set(Calendar.DATE, 31);
        c1.set(Calendar.YEAR, 2021);
        c2.set(Calendar.MONTH, 9);
        c2.set(Calendar.DATE, 01);
        c2.set(Calendar.YEAR, 2021);
        
        Date fechaMin = c1.getTime();
        Date fechaMax = c2.getTime();
        if((inicio.after(fechaMin) == true) && (inicio.before(fechaMax) == true) && (fin.before(fechaMax) == true) && (fin.before(fechaMax) == true)){
            return false;
        }
        return true;
    }
    
    void add_route(){
        String route_name = txtRouteName.getText();
        String route_distance = txtDistance.getText();
        Date start_date = jdcFechaInicio.getDate();
        long d1 = start_date.getTime();
        java.sql.Date fechaInicio = new java.sql.Date(d1);
        Date end_date = jdcFin.getDate();
        long d2 = end_date.getTime();
        java.sql.Date fechaFin = new java.sql.Date(d2);
        int start_hour = Integer.parseInt(txtHoraInicio.getText());
        int end_hour = Integer.parseInt(txtHoraFin.getText());
        int id_cyclist = Integer.parseInt(txtUserDocument.getText());
        
        if(route_name.isEmpty() || route_distance.isEmpty() || start_date.toString().isEmpty() || end_date.toString().isEmpty() || start_hour==0 || end_hour==0  ){
            JOptionPane.showMessageDialog(this, "Faltan Campos por diligenciar");
        }else{
            if(validacionFecha(start_date, end_date)==false){
                String query = "INSERT INTO `tb_route`(`route_name`, `route_distance`, `start_date`, `end_date`, `start_hour`, `end_hour`, `id_cyclist`) VALUES ('"+route_name+"','"+route_distance+"','"+fechaInicio+"','"+fechaFin+"','"+start_hour+"','"+end_hour+"','"+id_cyclist+"')";
                try{
                    cn = con.getConnection();
                    st = cn.createStatement();
                    st.executeUpdate(query);
                    clear_rows_table();
                    show_routes();
                }catch(SQLException e){
                        JOptionPane.showConfirmDialog(this, "Error -> " + e.getMessage());
                }
            }else{
                JOptionPane.showMessageDialog(this, "No esta dentro del rango de la fecha predeterminado!");
            }
        }
        
    }
    
